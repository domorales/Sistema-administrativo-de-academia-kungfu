CREATE DATABASE KUNGKUPANDA;  
USE KUNGKUPANDA;  

CREATE TABLE Categoria (  
Categoria VARCHAR(20),  
Peso FLOAT NOT NULL  CHECK (peso>0), 
PRIMARY KEY(Categoria)  
);  

CREATE TABLE Torneo (  
ID INT,  
Categoria VARCHAR(20) NOT NULL,  
PRIMARY KEY(ID),  
FOREIGN KEY(Categoria) REFERENCES Categoria (Categoria)  
);  

CREATE TABLE Estudiante (  
Cedula INT,  
Nombre_Completo VARCHAR(50) NOT NULL,  
Edad INT  NOT NULL,  
PRIMARY KEY(Cedula)  
);  

CREATE TABLE Compite (  
idCompetencia INT,   
Torneo int,  
Estudiante INT,   
FechaCompite DATE NOT NULL,  
PRIMARY KEY(idCompetencia),  
FOREIGN KEY(Torneo) REFERENCES Torneo (ID),  
FOREIGN KEY(Estudiante) REFERENCES Estudiante (Cedula)  
);  


CREATE TABLE Empleado(  
ID_Empleado INT,  
Nombre_completo VARCHAR(50) NOT NULL,  
Fecha_Nacimiento DATE NOT NULL,  
Correo_Electronico VARCHAR(30),  
Direccion_Domicilio VARCHAR(50),  
Teléfono_Contacto VARCHAR(15) NOT NULL,  
PRIMARY KEY(ID_Empleado)  
);  

CREATE TABLE Gana (  
idGanador INT,  
Trofeo VARCHAR(10) NOT NULL,  
Torneo INT,  
Estudiante INT,  
PRIMARY KEY(idGanador),  
FOREIGN KEY(Torneo) REFERENCES Torneo (ID),  
FOREIGN KEY(Estudiante) REFERENCES Estudiante (Cedula)  
);  

CREATE TABLE Cinturon (  
ID INT,  
Color VARCHAR(15) NOT NULL,  
Estudiante INT,  
Tiempo DATE NOT NULL,  
PRIMARY KEY (ID),  
FOREIGN KEY (Estudiante) REFERENCES Estudiante (Cedula)  
);  

CREATE TABLE esFamiliar (  
idFamiliar INT,  
Estudiante INT,  
Familiar INT,  
Relacion VARCHAR(10) NOT NULL,  
PRIMARY KEY(idFamiliar),  
FOREIGN KEY(Estudiante) REFERENCES Estudiante (Cedula),  
FOREIGN KEY(Familiar) REFERENCES Estudiante (Cedula)  
);  

CREATE TABLE Profesor(  
ID_Empleado INT,  
Tipo_Cinturón VARCHAR(15) NOT NULL,  
FOREIGN KEY (ID_Empleado) REFERENCES Empleado (ID_Empleado)    
);  

CREATE TABLE Administracion(  
ID_Empleado INT,  
FOREIGN KEY (ID_Empleado) REFERENCES Empleado (ID_Empleado)    
);  

CREATE TABLE Mantenimiento(  
ID_Empleado INT,  
FOREIGN KEY (ID_Empleado) REFERENCES Empleado (ID_Empleado)    
);  

CREATE TABLE Clase(  
ID INT,  
Profesor INT,  
Fecha_clase DATE NOT NULL,  
PRIMARY KEY(ID),  
FOREIGN KEY (Profesor) REFERENCES Profesor(ID_Empleado)    
);  

CREATE TABLE Ejercicio(  
idEjercicio INT,  
Nombre_ejercicio VARCHAR(20) NOT NULL,  
PRIMARY KEY(idEjercicio)  
);  

CREATE TABLE Realiza(  
idGenador INT,  
Ejercicio INT,  
Clase INT,  
PRIMARY KEY(idGenador),  
FOREIGN KEY (Ejercicio) REFERENCES Ejercicio(idEjercicio ),  
FOREIGN KEY (Clase) REFERENCES Clase(ID)  
); 

CREATE TABLE Registra(  
ID INT,  
Estudiante INT,  
Clase INT, 
Dia_pago date, 
PRIMARY KEY (ID), 
FOREIGN KEY (Estudiante) REFERENCES Estudiante (cedula), 
FOREIGN KEY (CLASE) REFERENCES CLASE (ID)  
); 

CREATE TABLE Asiste( 
IdAsistencia INT, 
FechaAsiste date, 
Estudiante INT, 
Clase INT, 
PRIMARY KEY (IdAsistencia), 
FOREIGN KEY (Estudiante) REFERENCES Estudiante (cedula), 
FOREIGN KEY (Clase) REFERENCES CLASE(ID) 
); 

